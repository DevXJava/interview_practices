package com.academian;

public class StringCharPassword {

	public static void main(String[] args) {
		
		String strPassword = "Abc@1234";
		char[] charPassword = {'A','b','c','@','1','2','3','4'};
		System.out.println("strPassword: " + strPassword);
		System.out.println("charPassword: " + charPassword);
		System.out.println("strPassword: " + String.valueOf(strPassword));
	}

}
